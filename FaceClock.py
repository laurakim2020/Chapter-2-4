import turtle
wn = turtle.Screen()
wn.bgcolor("lightgreen")
tess = turtle.Turtle()
tess.color("blue")
tess.pensize(3)

tess.stamp
tess.shape("turtle")
for i in range(12):
    tess.stamp()
    tess.penup()
    tess.forward(20)
    tess.pendown()
    tess.forward(150)
    tess.penup()
    tess.forward(20)
    tess.setpos(0,0)
    tess.left(360/12)
tess.hideturtle()
    
   
